package com.polopoly.ps.tools.collections.exception;

public class NoSuchEntryException extends Exception {

	public NoSuchEntryException() {
		super();
	}

	public NoSuchEntryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NoSuchEntryException(String arg0) {
		super(arg0);
	}

	public NoSuchEntryException(Throwable arg0) {
		super(arg0);
	}

}
