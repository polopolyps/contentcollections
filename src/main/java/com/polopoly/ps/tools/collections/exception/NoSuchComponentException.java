package com.polopoly.ps.tools.collections.exception;

public class NoSuchComponentException extends Exception {

	public NoSuchComponentException() {
		super();
	}

	public NoSuchComponentException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NoSuchComponentException(String arg0) {
		super(arg0);
	}

	public NoSuchComponentException(Throwable arg0) {
		super(arg0);
	}

}
